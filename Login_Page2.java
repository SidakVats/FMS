    boolean validation()
    {   
        uname= jTextField2.getText();
        ppassword= jPasswordField1.getText();
        
        if (uname.equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please enter User Name ");
        }
        else if (ppassword.equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please  enter Password ");
        }
        return true;
    }
    
    public void userVerification1(String UserName, String Password)
    {
        try 
        {
            Connection conn       = DriverManager.getConnection(path, username, password);
            String sql           = "select * from signup_page where UserName=? and Password=?";
            PreparedStatement st = conn.prepareStatement(sql);
            
            st.setString(1, UserName);
            st.setString(2, Password);
            ResultSet rs = st.executeQuery();
            
            if (rs.next())
            {
                Home_Page home = new Home_Page();
                home.show();
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Wrong UserName or Password");
            }
        } 
        catch (Exception e)
        {
             JOptionPane.showMessageDialog(this, "Error : "+e.getMessage());
        }
    }
    
